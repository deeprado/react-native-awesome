export default class TimeUtil {
  // 是否过时
  static checkDate(longTime) {
    let cDate = new Date();
    let tDate = new Date();
    tDate.setTime(longTime);
    if (cDate.getMonth() !== tDate.getMonth()) {
      return false;
    }
    if (cDate.getDay() !== tDate.getDay()) {
      return false;
    }
    if (cDate.getHours() - tDate.getHours() > 4) {
      return false;
    }
    return true;
  }
}
